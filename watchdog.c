#include"watchdog.h"
/*********************************************************************************
 * 								Function Definitions							 *
 *********************************************************************************/
void WATCHDOG_on(WATCHDOG_5V_timeOut a_prescale)
{
	SET_BIT(WDTCR , WDE);
	WDTCR |= a_prescale;
}
void WATCHDOG_off(void)
{
	WDTCR |= (1<<WDE) | (1<<WDTOE);
	_delay_us(4);
	CLEAR_BIT(WDTCR , WDE);
}
